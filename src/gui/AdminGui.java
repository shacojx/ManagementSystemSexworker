    public void loadcb() {
        arealist = new areaDAO().getAll();
        combodf = new DefaultComboBoxModel();
        
        for (area t : arealist) {
            combodf.addElement(t.getArea());
        }
        combo.setModel(combodf);
        
    }
    
    public ArrayList<mem> listcv() {
        ArrayList<mem> list = new ArrayList<>();
        for (mem t : memlist) {
            if (t.getId_area() == index) {
                list.add(t);
            }
            
        }
        return list;
    }
    
    public void loadtables() {
        memlist = new memDAO().getAll();
        tabledf = new DefaultTableModel();
        tabledf.addColumn("ID");
        tabledf.addColumn("Name");
        tabledf.addColumn("Year birtday");
        
        Vector<String> tblist;
        
        for (mem t : memlist) {
            if (t.getId_area() == index) {
                tblist = new Vector();
                tblist.add(t.getId() + "");
                tblist.add(t.getName());
                tblist.add(t.getYear() + "");
                tabledf.addRow(tblist);
            }
            tablesmem.setModel(tabledf);
        }
        
    }

