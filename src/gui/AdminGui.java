    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        // TODO add your handling code here:

        int check = tablesmem.getSelectedRow();
        
        int id = listtables.get(check).getId();
        String name = listtables.get(check).getName();
        int year = listtables.get(check).getYear();
        String phone = listtables.get(check).getPhone();
        int price = listtables.get(check).getPrice();
        int if_area = combo.getSelectedIndex() + 1;
        String height = listtables.get(check).getHeight();
        int vong1 = listtables.get(check).getVong1();
        int vong2 = listtables.get(check).getVong2();
        int vong3 = listtables.get(check).getVong3();
        String service = listtables.get(check).getService();
        mem m = new mem(id, name, year, phone, price, if_area, height, vong1, vong2, vong3, service);
        memd.delete(m);
        
        JOptionPane.showMessageDialog(this, "Delete succesfully !!!");
        loadtables();

    }//GEN-LAST:event_deleteActionPerformed

